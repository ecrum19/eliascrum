#!/usr/bin/env node
import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "..");

const publicationsDataPath = path.join(repoRoot, "src", "data", "publicationsData.ts");
const scholarCitationsPath = path.join(repoRoot, "src", "data", "scholarCitations.ts");
const scholarUrl =
  "https://scholar.google.com/citations?user=fAJsN2kAAAAJ&hl=en&cstart=0&pagesize=100";
const thesisDownloadTargets = [
  {
    publicationId: "engineering-coliphages-2022",
    url: "https://ecommons.luc.edu/luc_theses/4434/",
  },
];

const htmlEntityMap = {
  "&amp;": "&",
  "&quot;": '"',
  "&#39;": "'",
  "&lt;": "<",
  "&gt;": ">",
  "&nbsp;": " ",
};

function decodeHtml(value) {
  return value
    .replace(/&(amp|quot|#39|lt|gt|nbsp);/g, (entity) => htmlEntityMap[entity] ?? entity)
    .replace(/&#(\d+);/g, (_, codePoint) => String.fromCharCode(Number(codePoint)));
}

function normalizeTitle(value) {
  return decodeHtml(value)
    .toLowerCase()
    .replace(/<[^>]+>/g, "")
    .replace(/[^a-z0-9]+/g, " ")
    .trim();
}

function extractPublicationsFromData(source) {
  const publicationPattern = /id:\s*"([^"]+)"[\s\S]*?title:\s*"([^"]+)"/g;
  const entries = [];
  let match;

  while ((match = publicationPattern.exec(source)) !== null) {
    const [, id, title] = match;
    entries.push({
      id,
      title,
      normalizedTitle: normalizeTitle(title),
    });
  }

  return entries;
}

function parseCitationCount(value) {
  const digits = decodeHtml(value).replace(/[^\d]/g, "");
  if (!digits) {
    return 0;
  }

  return Number.parseInt(digits, 10);
}

function extractScholarEntries(html) {
  const rowPattern = /<tr class="gsc_a_tr"[\s\S]*?<\/tr>/g;
  const rows = html.match(rowPattern) ?? [];

  return rows
    .map((row) => {
      const titleMatch = row.match(/class="gsc_a_at"[^>]*>([\s\S]*?)<\/a>/);
      const citationMatch = row.match(/class="gsc_a_ac[^"]*"[^>]*>([\s\S]*?)<\/a>/);

      if (!titleMatch) {
        return null;
      }

      const title = decodeHtml(titleMatch[1]).replace(/\s+/g, " ").trim();
      const citationCount = parseCitationCount(citationMatch?.[1] ?? "");

      return {
        title,
        normalizedTitle: normalizeTitle(title),
        citationCount,
      };
    })
    .filter(Boolean);
}

function findBestScholarMatch(publication, scholarEntries) {
  const exactMatch = scholarEntries.find(
    (entry) => entry.normalizedTitle === publication.normalizedTitle
  );
  if (exactMatch) {
    return exactMatch;
  }

  const substringMatches = scholarEntries.filter((entry) => {
    return (
      entry.normalizedTitle.includes(publication.normalizedTitle) ||
      publication.normalizedTitle.includes(entry.normalizedTitle)
    );
  });

  if (substringMatches.length === 0) {
    return null;
  }

  substringMatches.sort((a, b) => {
    const aDistance = Math.abs(a.normalizedTitle.length - publication.normalizedTitle.length);
    const bDistance = Math.abs(b.normalizedTitle.length - publication.normalizedTitle.length);
    return aDistance - bDistance;
  });

  return substringMatches[0];
}

function formatGeneratedFile(lastUpdatedIso, citationsByPublicationId, downloadsByPublicationId) {
  const citationLines = Object.entries(citationsByPublicationId).map(([id, count]) => {
    const value = count === null ? "null" : String(count);
    return `  "${id}": ${value},`;
  });

  const downloadLines = Object.entries(downloadsByPublicationId).map(([id, count]) => {
    const value = count === null ? "null" : String(count);
    return `  "${id}": ${value},`;
  });

  return `/* eslint-disable */
// This file is auto-generated by scripts/update-scholar-citations.mjs
// Run manually with: npm run update:scholar

export const scholarCitationLastUpdatedIso: string | null = "${lastUpdatedIso}";

export const scholarCitationsByPublicationId: Record<string, number | null> = {
${citationLines.join("\n")}
};

export const publicationDownloadsByPublicationId: Record<string, number | null> = {
${downloadLines.join("\n")}
};
`;
}

async function fetchScholarHtml() {
  const response = await fetch(scholarUrl, {
    headers: {
      "user-agent":
        "Mozilla/5.0 (compatible; EliasWebsiteBot/1.0; +https://github.com/)",
      "accept-language": "en-US,en;q=0.9",
    },
  });

  if (!response.ok) {
    throw new Error(`Google Scholar request failed with status ${response.status}`);
  }

  const html = await response.text();
  if (/not a robot|unusual traffic|captcha/i.test(html)) {
    throw new Error("Google Scholar returned a bot-check page; citation update aborted.");
  }

  return html;
}

function parseDownloadCountFromHtml(html) {
  const spanMatch = html.match(
    /<span[^>]*id=["']article-downloads["'][^>]*>\s*([\d,]+)\s*<\/span>/i,
  );
  if (spanMatch) {
    const digitsOnly = spanMatch[1].replace(/[^\d]/g, "");
    if (digitsOnly) {
      return Number.parseInt(digitsOnly, 10);
    }
  }

  return null;
}

function parseStatsRecordIdFromHtml(html) {
  const match = html.match(/insertDownloads\((\d+)\)/i);
  if (!match) {
    return null;
  }

  return match[1];
}

function parseDownloadCountFromApiValue(value) {
  const digitsOnly = String(value ?? "").replace(/[^\d]/g, "");
  if (!digitsOnly) {
    return null;
  }

  return Number.parseInt(digitsOnly, 10);
}

async function fetchDownloadCountFromStatsApi(url, statsRecordId) {
  const apiUrl = new URL(`/do/api/site/stats/${statsRecordId}/json`, url);
  const response = await fetch(apiUrl, {
    headers: {
      "user-agent":
        "Mozilla/5.0 (compatible; EliasWebsiteBot/1.0; +https://github.com/)",
      "accept-language": "en-US,en;q=0.9",
      accept: "application/json, text/plain, */*",
    },
  });

  if (!response.ok) {
    throw new Error(`Thesis stats API request failed with status ${response.status}`);
  }

  const payload = await response.json();
  return parseDownloadCountFromApiValue(payload?.total_downloads);
}

async function fetchThesisDownloadCount(url) {
  const response = await fetch(url, {
    headers: {
      "user-agent":
        "Mozilla/5.0 (compatible; EliasWebsiteBot/1.0; +https://github.com/)",
      "accept-language": "en-US,en;q=0.9",
    },
  });

  if (!response.ok) {
    throw new Error(`Thesis page request failed with status ${response.status}`);
  }

  const html = await response.text();
  const htmlDownloadCount = parseDownloadCountFromHtml(html);
  if (htmlDownloadCount !== null) {
    return htmlDownloadCount;
  }

  const statsRecordId = parseStatsRecordIdFromHtml(html);
  if (!statsRecordId) {
    return null;
  }

  return fetchDownloadCountFromStatsApi(url, statsRecordId);
}

async function main() {
  const publicationsSource = await fs.readFile(publicationsDataPath, "utf8");
  const publications = extractPublicationsFromData(publicationsSource);

  if (publications.length === 0) {
    throw new Error("No publications found in publicationsData.ts");
  }

  const scholarHtml = await fetchScholarHtml();
  const scholarEntries = extractScholarEntries(scholarHtml);

  if (scholarEntries.length === 0) {
    throw new Error("No publication rows were parsed from Google Scholar response.");
  }

  const citationsByPublicationId = {};
  const unmatched = [];
  const downloadsByPublicationId = {};

  publications.forEach((publication) => {
    const match = findBestScholarMatch(publication, scholarEntries);
    if (!match) {
      citationsByPublicationId[publication.id] = null;
      unmatched.push(publication.title);
    } else {
      citationsByPublicationId[publication.id] = match.citationCount;
    }

    downloadsByPublicationId[publication.id] = null;
  });

  for (const target of thesisDownloadTargets) {
    const publicationExists = publications.some(
      (publication) => publication.id === target.publicationId
    );
    if (!publicationExists) {
      continue;
    }

    try {
      const downloadCount = await fetchThesisDownloadCount(target.url);
      downloadsByPublicationId[target.publicationId] = downloadCount;
    } catch (error) {
      console.warn(
        `Failed to fetch thesis download count for ${target.publicationId}: ${
          error instanceof Error ? error.message : String(error)
        }`,
      );
    }
  }

  const lastUpdatedIso = new Date().toISOString();
  const generatedFile = formatGeneratedFile(
    lastUpdatedIso,
    citationsByPublicationId,
    downloadsByPublicationId,
  );
  await fs.writeFile(scholarCitationsPath, generatedFile, "utf8");

  console.log(`Updated citation counts for ${publications.length} publications.`);
  if (unmatched.length > 0) {
    console.warn("Unmatched publications:");
    unmatched.forEach((title) => console.warn(`- ${title}`));
  }
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exitCode = 1;
});
